let bulkCreateSchedule = (data) => {
  return new Promise(async (resolve, reject) => {
    try {
      if (!data.arrSchedule || !data.doctorId || !data.formatedDate) {
        resolve({
          errCode: -1,
          errCode: "Missing required paramenter!",
        });
      } else {
        let schedule = data.arrSchedule;
        if (schedule && schedule.length > 0) {
          schedule = schedule.map((item) => {
            item.max_number = MAX_NUMBER_SCHEDULE;
            return item;
          });
        }
        let existing = await db.Schedule.findAll({
          where: { doctorId: data.doctorId, date: data.formatedDate },
          attributes: ["timeType", "date", "doctorId", "maxNumber"],
          raw: true,
        });
        let toCreate = _.differenceWith(schedule, existing, (a, b) => {
          return a.timeType === b.timeType && +a.date === +b.date;
        });
        if (toCreate && toCreate.length > 0) {
          await db.Schedule.bulkCreate(toCreate);
        }
        resolve({
          errCode: 0,
          errMessage: "OK",
        });
      }
    } catch (e) {
      reject(e);
    }
  });
};
let getScheduleByDate = (doctorId, dateTimestamp) => {
  return new Promise(async (resolve, reject) => {
    try {
      if (!doctorId || !dateTimestamp || isNaN(dateTimestamp)) {
        return resolve({
          errCode: 1,
          errMessage: "Missing or invalid required parameter!",
        });
      }

      let dateObj = new Date(Number(dateTimestamp));

      if (isNaN(dateObj.getTime())) {
        return resolve({
          errCode: 2,
          errMessage: "Invalid timestamp format!",
        });
      }

      // Điều chỉnh timestamp về UTC trước khi lấy ngày
      dateObj.setMinutes(dateObj.getMinutes() - dateObj.getTimezoneOffset());
      let date = dateObj.toISOString().split("T")[0]; // YYYY-MM-DD

      let dataSchedule = await db.Schedule.findAll({
        where: {
          doctorId: doctorId,
          date: date,
        },
        include: [
          {
            model: db.Allcode,
            as: "timeTypeData",
            attributes: ["valueEn", "valueVi"],
          },
          {
            model: db.User,
            as: "doctorData",
            attributes: ["firstName", "lastName"],
          },
        ],
        raw: false,
        nest: true,
      });

      resolve({
        errCode: 0,
        data: dataSchedule,
      });
    } catch (e) {
      reject(e);
    }
  });
};

